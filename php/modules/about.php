  <h2>Somos uma das maiores distribuidoras de gelo da cidade de sorocaba!</h2>
  <p>A Rigelo, uma distribuidora de gelo fundada de maneira independente em 2018, surpreendeu seus clientes iniciais,
    que não imaginavam o alcance que a empresa atingiria. Tudo começou com uma única máquina de gelo destinada a eventos
    sociais, onde amigos e familiares começaram a solicitar sacos de gelo para resfriar suas bebidas, transformando a
    empresa em um reconhecido "produtor" de gelo.
    O que começou como uma brincadeira rapidamente se expandiu. A Rigelo iniciou suas operações com apenas uma câmara
    fria e, hoje, orgulha-se de possuir duas câmaras enormes com um impressionante poder de resfriamento, atingindo uma
    temperatura de armazenamento de menos 15 graus Celsius!
    A empresa conquistou a preferência de muitos comerciantes devido à excelente qualidade dos produtos fornecidos por
    seus parceiros e à eficiente forma de armazenamento. Atualmente, a Rigelo atende a mais de 30 estabelecimentos
    comerciais, oferecendo freezers sem burocracia e assegurando envios de pedidos de maneira eficaz.
    Se você deseja fazer parte do império Rigelo, não hesite em entrar em contato conosco para explorar oportunidades de
    colaboração!</p>

